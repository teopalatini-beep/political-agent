#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const DEFAULT_INPUT = path.join(ROOT, "affiliate.metrics.sample.csv");
const OUTPUT_PATH = path.join(ROOT, "AFFILIATE_KPI_SUMMARY.md");

function parseArgs(argv) {
  const args = {};
  for (const arg of argv) {
    if (!arg.startsWith("--")) continue;
    const [k, ...rest] = arg.slice(2).split("=");
    args[k] = rest.length ? rest.join("=") : "true";
  }
  return args;
}

function parseCsv(raw) {
  const lines = raw.trim().split(/\r?\n/);
  const headers = lines[0].split(",");
  return lines.slice(1).map((line) => {
    const values = line.split(",");
    const row = {};
    headers.forEach((h, i) => {
      row[h] = values[i];
    });
    return row;
  });
}

function toNumber(value) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : 0;
}

function aggregateByOffer(rows) {
  const map = new Map();
  for (const row of rows) {
    const key = row.offer;
    if (!map.has(key)) {
      map.set(key, {
        offer: key,
        impressions: 0,
        views3s: 0,
        linkClicks: 0,
        emailOptins: 0,
        conversions: 0,
        revenueUSD: 0,
        refunds: 0,
      });
    }
    const item = map.get(key);
    item.impressions += toNumber(row.impressions);
    item.views3s += toNumber(row.views3s);
    item.linkClicks += toNumber(row.linkClicks);
    item.emailOptins += toNumber(row.emailOptins);
    item.conversions += toNumber(row.conversions);
    item.revenueUSD += toNumber(row.revenueUSD);
    item.refunds += toNumber(row.refunds);
  }
  return [...map.values()].map((item) => {
    const ctr = item.impressions > 0 ? (item.linkClicks / item.impressions) * 100 : 0;
    const optinRate = item.linkClicks > 0 ? (item.emailOptins / item.linkClicks) * 100 : 0;
    const cvr = item.linkClicks > 0 ? (item.conversions / item.linkClicks) * 100 : 0;
    const epc = item.linkClicks > 0 ? item.revenueUSD / item.linkClicks : 0;
    const refundRate = item.conversions > 0 ? (item.refunds / item.conversions) * 100 : 0;
    return {
      ...item,
      ctr: Math.round(ctr * 100) / 100,
      optinRate: Math.round(optinRate * 100) / 100,
      cvr: Math.round(cvr * 100) / 100,
      epc: Math.round(epc * 100) / 100,
      refundRate: Math.round(refundRate * 100) / 100,
    };
  });
}

function renderSummary(sorted) {
  const top = sorted.slice(0, 3);
  const bottom = sorted.slice(-2);
  const lines = [
    "# Affiliate KPI Summary",
    "",
    "Generated by `npm run affiliate:kpi`.",
    "",
    "## Top Offers",
    "",
    "| Offer | Revenue | Conversions | EPC | CVR (%) |",
    "|---|---:|---:|---:|---:|",
    ...top.map(
      (x) => `| ${x.offer} | ${x.revenueUSD.toFixed(2)} | ${x.conversions} | ${x.epc.toFixed(2)} | ${x.cvr.toFixed(2)} |`
    ),
    "",
    "## Offers to Review",
    "",
    "| Offer | Revenue | Clicks | Conversions | Notes |",
    "|---|---:|---:|---:|---|",
    ...bottom.map(
      (x) =>
        `| ${x.offer} | ${x.revenueUSD.toFixed(2)} | ${x.linkClicks} | ${x.conversions} | Replace or retest hook + CTA this week |`
    ),
    "",
    "## Decision Rules",
    "",
    "- Scale offers with >= 2 conversions and positive EPC trend.",
    "- Pause offers with >= 100 clicks and no conversions.",
    "- Replace bottom 20% every week.",
    "",
  ];
  return lines.join("\n");
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const input = args.input ? path.resolve(ROOT, args.input) : DEFAULT_INPUT;
  if (!fs.existsSync(input)) {
    throw new Error(`Input CSV not found: ${input}`);
  }

  const raw = fs.readFileSync(input, "utf8");
  const rows = parseCsv(raw);
  const aggregated = aggregateByOffer(rows);
  aggregated.sort((a, b) => b.epc - a.epc);

  const md = renderSummary(aggregated);
  fs.writeFileSync(OUTPUT_PATH, md, "utf8");
  console.log("KPI summary generated:");
  console.log(`- ${OUTPUT_PATH}`);
}

main();
